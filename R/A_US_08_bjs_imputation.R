writeLines(
  c("% Auto-generated by A_US_08_bjs_imputation.R",
    paste0("% Generated: ", Sys.time()),
    macro("AUSBJSNSpecs", length(results))),
  here("output", "tables", "a_us_bjs_macros.tex")
)

sink()
